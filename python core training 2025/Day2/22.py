numlst = [10,20,30,40]

print(numlst)    #[10,20,30,40]

numlst.append(50)   

print(numlst)    #[10,20,30,40,50]

numlst.pop(0)    

print(numlst)   # [20,30,40,50]

numlst.remove(40) 

print(numlst)   # [20,30,50]

numlst.reverse()

print(numlst)   # [50,30,20]

